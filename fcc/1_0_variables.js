// Existen 3 formas de declarar variables

// esto se puede usar en todo el programa
var myName = "Francisco" 

// esto sólo se usa en el scope donde fue declarado
let ourName = "Nuestro nombre"

// esto es una constante y no se puede cambiar
const pi = 3.141592653

// esto es declarar una variable
var a;

// esto es declarar y asignar una variable
var b = 2;
console.log(a)
// esto es asignar
a = 7;

// puedo hacer 
