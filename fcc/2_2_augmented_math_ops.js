// existe un atajo para hacer esto

// SUMA
a += 12;
b += 9;
c += 7;
console.log(a,b,c);

// RESTA
a -= 12;
b -= 9;
c -= 7;
console.log(a,b,c);

// MULTIPLICACION
a *= 5; // a = a * 5
b *= 3; // b = b * 3
c *= 10; // c = c * 10
console.log(a,b,c);

// DIVISION
a /= 5; // a = a / 5
b /= 3; // b = b / 3
c /= 10; // c = c / 10
console.log(a,b,c);